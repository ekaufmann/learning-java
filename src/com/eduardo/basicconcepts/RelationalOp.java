public class RelationalOp {

    public static void main(String[] args) {

        int val1 = 1;
        int val2 = 2;

        System.out.println(val1 + " >= " + val2 + ": " + (val1 >= val2));
        System.out.println(val1 + " <= " + val2 + ": " + (val1 <= val2));
        System.out.println(val1 + " > " + val2 + ": " + (val1 > val2));
        System.out.println(val1 + " < " + val2 + ": " + (val1 < val2));
        System.out.println(val1 + " != " + val2 + ": " + (val1 != val2));
        System.out.println(val1 + " == " + val2 + ": " + (val1 == val2));

    }
}